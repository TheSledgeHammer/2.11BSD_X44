#include <sys/cdefs.h>
#include <sys/param.h>
#include <sys/systm.h>
#include <sys/errno.h>
#include <sys/exec.h>
#include <sys/exec_aout.h>
#include <sys/exec_linker.h>

static int
exec_nomid(p, elp)
	struct proc *p;
	struct exec_linker *elp;
{
	int error;
	u_long midmag, magic;
	u_short mid;
	struct exec *execp = elp->el_image_hdr;

	midmag = ntohl(execp->a_midmag);
	mid = (midmag >> 16) & 0xffff;
	magic = midmag & 0xffff;

	if(magic == 0) {
		magic = (execp->a_midmag & 0xffff);
		mid = MID_ZERO;
	}

	midmag = mid << 16 | magic;

	switch (midmag) {
	case (MID_ZERO << 16) | QMAGIC:
			error = exec_aout_prep_zmagic(p, elp);
			break;
	default:
		error = ENOEXEC;
	}
	return error;
}

int
cpu_exec_aout_linker(p, elp)
	struct proc *p;
	struct exec_linker *elp;
{
	int error = ENOEXEC;
	if((error == exec_nomid(p, elp)) == 0) {
		return error;
	}
	return error;
}
