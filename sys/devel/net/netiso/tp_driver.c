int
tp_driver(struct tpipcb *p, struct tp_event *e)
{
	register int index, error = 0;
	struct act_ent *a;

	index = inx[1 + e->ev_number][p->tpp_state];
	if (index < 0) {
		index = _Xebec_index(e, p);
	}
	if (index == 0) {
		a = &erroraction;
	} else {
		a = &statetable[index];
	}
	if (a->a_action) {
		error = _Xebec_action(a->a_action, e, p);
	}
	if (error == 0) {
		p->tpp_state = a->a_newstate;
	}
	return (error);
}
